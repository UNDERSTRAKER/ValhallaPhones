function displayproducts(nameProduct, nameId) {
    const productSection = document.getElementById(nameId);

    nameProduct.forEach(product => {
        const card = document.createElement('article');
        card.classList.add('tarjetas');
        card.innerHTML = `
        <link rel="stylesheet" href="./style.css">
        <div class="card  border-0 ho" style="width: 18rem;">
            <a href="${product.direccion}" class="text-decoration-none text-reset">
                <img src="${product.img}" class="card-img-top" alt="${product.name}">
                <div class="card-body ">
                  <p class="card-text text-center">${product.name}</p>
                  <p class="card-text text-center">${product.precio}</p>
                </div>
            </a>
        </div>
        `

        productSection.append(card);
    });

}







